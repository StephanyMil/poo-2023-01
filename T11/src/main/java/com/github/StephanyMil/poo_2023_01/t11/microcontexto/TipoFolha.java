package com.github.StephanyMil.poo_2023_01.t11.microcontexto;

public class TipoFolha {
    private String forma;
    private String cor;

    public TipoFolha(String forma, String cor) {
        this.forma = forma;
        this.cor = cor;
    }

    public String getForma() {
        return forma;
    }

    public String getCor() {
        return cor;
    }
}
